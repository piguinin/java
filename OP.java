public class OP {
    
    public double result;
    
    public void calsuma(double n1,double n2){
        result =  n1 + n2;
    }
        public void calresta(double n1,double n2){
        result =  n1 - n2;
    }
        public void caldivi(double n1,double n2){
        result =  n1 / n2;
    }
        public void calmulti(double n1,double n2){
        result =  n1 * n2;
    }
        public void calmulpote(double n1,double n2){
        result =  n1 * n1 + n2 * n2;
    }
        public void calmulporce(double n1,double n2){
        result =  n1 / 100 * n2;
    }
     public void calmulraiz(double n1,double n2){
        result =  n1 * 2 / n2;    
    }

}
